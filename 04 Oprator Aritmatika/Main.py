x = 8 
y = 4

# Oprasi tambah +
hasil = x + y
print(x,'+',y,'=',hasil)

# Oprasi kurang -
hasil = x - y
print(x,'-',y,'=',hasil)

# Oprasi kali *
hasil = x * y
print(x,'*',y,'=',hasil)

# Oprasi bagi /
hasil = x / y
print(x,'/',y,'=',hasil)

# Oprasi pangkat (eksponen) **
hasil = x ** y
print(x,'**',y,'=',hasil)

# Oprasi sisa hasil bagi (modulus) %
hasil = x % y
print(x,'%',y,'=',hasil)

# Oprasi floor division (pembagian dengan pembulatan ke bawah) //
hasil = x // y
print(x,'//',y,'=',hasil)




